# VGG style Discriminator
class Discriminator_VGG(nn.Module):
    def __init__(
        self,
        size,
        in_nc,
        base_nf,
        norm_type="batch",
        act_type="leakyrelu",
        mode="CNA",
        convtype="Conv2D",
        arch="ESRGAN",
    ):
        super(Discriminator_VGG, self).__init__()

        conv_blocks = []
        conv_blocks.append(
            B.conv_block(
                in_nc,
                base_nf,
                kernel_size=3,
                stride=1,
                norm_type=None,
                act_type=act_type,
                mode=mode,
            )
        )
        conv_blocks.append(
            B.conv_block(
                base_nf,
                base_nf,
                kernel_size=4,
                stride=2,
                norm_type=norm_type,
                act_type=act_type,
                mode=mode,
            )
        )

        cur_size = size // 2
        cur_nc = base_nf
        while cur_size > 4:
            out_nc = cur_nc * 2 if cur_nc < 512 else cur_nc
            conv_blocks.append(
                B.conv_block(
                    cur_nc,
                    out_nc,
                    kernel_size=3,
                    stride=1,
                    norm_type=norm_type,
                    act_type=act_type,
                    mode=mode,
                )
            )
            conv_blocks.append(
                B.conv_block(
                    out_nc,
                    out_nc,
                    kernel_size=4,
                    stride=2,
                    norm_type=norm_type,
                    act_type=act_type,
                    mode=mode,
                )
            )
            cur_nc = out_nc
            cur_size //= 2

        self.features = B.sequential(*conv_blocks)

        # classifier
        if arch == "PPON":
            self.classifier = nn.Sequential(
                nn.Linear(cur_nc * cur_size * cur_size, 128),
                nn.LeakyReLU(0.2, True),
                nn.Linear(128, 1),
            )
        else:  # arch='ESRGAN':
            self.classifier = nn.Sequential(
                nn.Linear(cur_nc * cur_size * cur_size, 100),
                nn.LeakyReLU(0.2, True),
                nn.Linear(100, 1),
            )

    def forward(self, x):
        x = self.features(x)
        x = x.view(x.size(0), -1)
        x = self.classifier(x)
        return x


