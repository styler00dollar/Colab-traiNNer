class NLayerDiscriminator(nn.Module):
    r"""
    PatchGAN discriminator
    https://arxiv.org/pdf/1611.07004v3.pdf
    https://arxiv.org/pdf/1803.07422.pdf
    """

    def __init__(
        self,
        input_nc,
        ndf=64,
        n_layers=3,
        norm_layer=nn.BatchNorm2d,
        use_sigmoid=False,
        get_feats=False,
        patch=True,
        use_spectral_norm=False,
    ):
        """Construct a PatchGAN discriminator
        Parameters:
            input_nc (int): the number of channels in input images
            ndf (int): the number of filters in the last conv layer
            n_layers (int): the number of conv layers in the discriminator
            norm_layer (nn.Module): normalization layer (if not using Spectral Norm)
            patch (bool): Select between an patch or a linear output
            use_spectral_norm (bool): Select if Spectral Norm will be used
        """
        super(NLayerDiscriminator, self).__init__()
        """
        if type(norm_layer) == functools.partial:  # no need to use bias as BatchNorm2d has affine parameters
            use_bias = norm_layer.func == nn.InstanceNorm2d
        else:
            use_bias = norm_layer == nn.InstanceNorm2d
        """

        if use_spectral_norm:
            # disable Instance or Batch Norm if using Spectral Norm
            norm_layer = B.Identity

        self.get_feats = get_feats
        self.n_layers = n_layers
        # use_sigmoid  # not used for now

        use_bias = False
        kw = 4
        padw = 1  # int(np.ceil((kw-1.0)/2))

        sequence = [
            [
                B.add_spectral_norm(
                    nn.Conv2d(input_nc, ndf, kernel_size=kw, stride=2, padding=padw),
                    use_spectral_norm,
                ),
                nn.LeakyReLU(0.2, True),
            ]
        ]

        nf_mult = 1
        nf_mult_prev = 1
        for n in range(1, n_layers):
            # gradually increase the number of filters
            nf_mult_prev = nf_mult
            nf_mult = min(2**n, 8)
            sequence += [
                [
                    B.add_spectral_norm(
                        nn.Conv2d(
                            ndf * nf_mult_prev,
                            ndf * nf_mult,
                            kernel_size=kw,
                            stride=2,
                            padding=padw,
                            bias=use_bias,
                        ),
                        use_spectral_norm,
                    ),
                    norm_layer(ndf * nf_mult),
                    nn.LeakyReLU(0.2, True),
                ]
            ]

        nf_mult_prev = nf_mult
        nf_mult = min(2**n_layers, 8)
        sequence += [
            [
                B.add_spectral_norm(
                    nn.Conv2d(
                        ndf * nf_mult_prev,
                        ndf * nf_mult,
                        kernel_size=kw,
                        stride=1,
                        padding=padw,
                        bias=use_bias,
                    ),
                    use_spectral_norm,
                ),
                norm_layer(ndf * nf_mult),
                nn.LeakyReLU(0.2, True),
            ]
        ]

        if patch:
            # output as 1 channel prediction map patches
            sequence += [
                [
                    B.add_spectral_norm(
                        nn.Conv2d(
                            ndf * nf_mult, 1, kernel_size=kw, stride=1, padding=padw
                        ),
                        use_spectral_norm,
                    )
                ]
            ]
        else:
            # linear vector classification output
            sequence += [[B.Mean([1, 2]), nn.Linear(ndf * nf_mult, 1)]]

        if use_sigmoid:
            sequence += [[nn.Sigmoid()]]

        if get_feats:
            for n in range(len(sequence)):
                setattr(self, "model" + str(n), nn.Sequential(*sequence[n]))
        else:
            sequence_stream = []
            for n in range(len(sequence)):
                sequence_stream += sequence[n]
            self.model = nn.Sequential(*sequence_stream)

    def forward(self, x, return_maps=False):
        if self.get_feats:
            res = [x]
            for n in range(self.n_layers + 2):
                model = getattr(self, "model" + str(n))
                res.append(model(res[-1]))
            if return_maps:
                return [res[-1], res[1:-1]]
            return res[-1]
        # Standard forward.
        # return self.model(x)

        # TODO: check
        result = self.model(x)
        return torch.mean(torch.mean(result, dim=2), dim=2)


